import random

a = random.randint(1,10)
b = random.randint(1,10)
print("a =", a)
print("b =", b)

# Arithmetic operators
print('Addition:', a + b)
print('Subtraction:', a - b)
print('Multiplication:', a * b)
print('Division:', a / b)
print('Modulus:', a % b)
print('Floor Division:', a // b)
print('Exponentiation:', a ** b)

# comperison operators
print('a > b:', a > b)
print('a < b:', a < b)
print('a == b:', a == b)
print('a != b:', a != b)
print('a >= b:', a >= b)
print('a <= b:', a <= b)

# Logical Operators
a, b = True, False
print('a and b:', a and b)
print('a or b:', a or b)
print('not a:', not a)